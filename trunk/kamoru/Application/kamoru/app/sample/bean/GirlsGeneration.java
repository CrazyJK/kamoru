package kamoru.app.sample.bean;

/**
 * @author  kamoru
 */
public class GirlsGeneration {

	/**
	 * @uml.property  name="num"
	 */
	private int num;
	/**
	 * @uml.property  name="name"
	 */
	private String name;
	/**
	 * @uml.property  name="age"
	 */
	private int age;
	
	/**
	 * @return
	 * @uml.property  name="num"
	 */
	public int getNum() {
		return num;
	}
	/**
	 * @param num
	 * @uml.property  name="num"
	 */
	public void setNum(int num) {
		this.num = num;
	}
	/**
	 * @return
	 * @uml.property  name="name"
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name
	 * @uml.property  name="name"
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return
	 * @uml.property  name="age"
	 */
	public int getAge() {
		return age;
	}
	/**
	 * @param age
	 * @uml.property  name="age"
	 */
	public void setAge(int age) {
		this.age = age;
	}

}
